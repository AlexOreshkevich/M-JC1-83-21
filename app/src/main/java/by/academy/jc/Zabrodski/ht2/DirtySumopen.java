package by.academy.jc.Zabrodski.ht2;

public class DirtySumopen {

  /**
   * @return - returns the sum of the values of the sum of numbers and the multiplication of numbers
   */
  public static int result(int a, int b) {

    return a + b + a * b;
  }

  public static long result(long a, long b) {

    return a + b + a * b;
  }

  public static double result(double a, double b){

    return a + b + a * b;
  }

  public static float result(float a, float b){

    return a + b + a * b;
  }

}
